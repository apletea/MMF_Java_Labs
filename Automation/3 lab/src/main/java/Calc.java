public class Calc {
    public static double add(double a, double b){
        return a + b;
    }

    public static double diff(double a, double b){
        return a - b;
    }

    public static double mult(double a, double b){
        return a*b;
    }

    public static double divv(double a, double b){
        return a/b;
    }
}
